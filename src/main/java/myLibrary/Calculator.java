package myLibrary;

public class Calculator {
    public static int Sum(int a, int b) {
        return a + b;
    }

    public static int Rest(int a, int b) {
        return a - b;
    }

    public static int Mult(int a, int b) {
        return a * b;
    }

    public static int Div(int a, int b) {
        return a / b;
    }
}
